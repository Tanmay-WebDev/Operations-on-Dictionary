# Operations on Dictionary

dict = {1,2,2,3,3,4,4,4,5,5,6,7}
print("Value of Dictionary : " , dict)

dict.add(8)
print("Value Of Dictionary After Adding 8 : " , dict)

dict.remove(4)
print("Value Of Dictionary After Removing 4 : " , dict)

dict.clear()
print("Value Of Dictionary After Clearing Dictionary : " , dict)

